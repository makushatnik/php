class AcceptanceTester extends \Codeception\Actor
{
    use _generated\AcceptanceTesterActions;

    public function seeContentIsLong($content, $trigger_len = 100) {
    	$this->assertGreaterThen($trigger_len, strlen($content));
    }

    public function assertGreaterThen($expected, $size) {
    	return $size > $expected ? true : false;
    }
}
